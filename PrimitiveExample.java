public class PrimitiveExample {
    public static void main(String[] args) {

        int umur = 20;
        double tinggi = 170.5;
        char grade = 'A';
        boolean lulus = true;

        System.out.println("Umur: " + umur);
        System.out.println("Tinggi: " + tinggi);
        System.out.println("Grade: " + grade);
        System.out.println("Status Lulus: " + lulus);
    }
}
